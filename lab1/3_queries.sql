-- Пример 1: Повышение цены на конкретный товар (id=1, Ноутбук) на 10%
UPDATE public.products
SET price = price * 1.10
WHERE id = 1;

-- Пример 2: Обновление статуса заказа.
-- Допустим, заказ №3 (был 'P') обработали и отправили сегодня.
UPDATE public.orders
SET
    status = 'A',
    ship_date = CURRENT_DATE
WHERE id = 3;

