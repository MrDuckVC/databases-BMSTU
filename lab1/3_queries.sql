-- =============================================================
-- ПУНКТ 10: Удаление данных (DELETE)
-- См. стр. 41 методички
-- =============================================================

-- ВАЖНО: Мы не можем просто удалить клиента, у которого есть заказы,
-- из-за ограничения внешнего ключа (Foreign Key Constraint).
-- Поэтому для демонстрации мы сначала создадим "лишнего" клиента,
-- а потом удалим его.

-- 1. Создаем временного клиента
INSERT INTO public.customers (last_name, first_name, company_name, city)
VALUES ('Тестовый', 'Удалятор', 'Temporary LLC', 'Воркута');

-- Проверяем, что он появился (id будет последним, например 13)
-- SELECT * FROM public.customers WHERE last_name = 'Тестовый';

-- 2. Удаляем этого клиента (Выполнение пункта 10)
DELETE FROM public.customers
WHERE last_name = 'Тестовый' AND first_name = 'Удалятор';


-- Пример 3: Удаление позиции из заказа (если ошибочно добавили товар)
-- Удаляем товар с product_id=2 (Мышь) из заказа №2
DELETE FROM public.items
WHERE order_id = 2 AND product_id = 2;

